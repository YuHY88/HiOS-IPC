	pgstStatTypeWholwInfo->pStatSubTpIndexPoolForSubTpQ =\
		(stStatSubTpNode*)((UINT8*)(pgstStatTypeWholwInfo->pStatSubTypeNodePool) + statSubTypeNum*sizeof(stStatSTCharacterDef));
