		(stStatTpNode*)((UINT8*)(pgstStatTypeWholwInfo->pStatSubTpIndexPoolForTpQ) + statSubTypeNum*sizeof(stStatSubTpNode));
	/*memory pointer for stat type's threshold(long and short, up and down)*/
	pgstStatTypeWholwInfo->pStatTpThrdSp =\
		(void *)((UINT8*)(pgstStatTypeWholwInfo->pStatTpIndexPoolForScanTpQ) + statSubTypeNum*sizeof(stStatSubTpNode));
	/*memory pointer for stat scan type' threshold (long and short, up and down)*/
	pgstStatTypeWholwInfo->pStatScanThrdSp = \
		(void *)((UINT8*)(pgstStatTypeWholwInfo->pStatTpThrdSp) + 4*statThrdSize);
	/*memory pointer for max stat data*/
	pgstStatTypeWholwInfo->pStatPoolDate = \
		(void *)((UINT8*)(pgstStatTypeWholwInfo->pStatScanThrdSp) + 4*statThrdSize);
	
	pgstStatTypeWholwInfo->statScanTypeNum = statScanTypeNum;
	pgstStatTypeWholwInfo->statTypeNum = statTypeNum;
	pgstStatTypeWholwInfo->statSubTypeNum = statSubTypeNum;
	
