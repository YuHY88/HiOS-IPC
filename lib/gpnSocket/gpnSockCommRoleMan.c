UINT32 gpnSockRoleCreat(UINT32 funRole, UINT32 *prole)
{
	UINT32 reVal;
	UINT32 runPlace;
	UINT32 selfSlot;
	UINT32 NMaSlot;
	UINT32 NMbSlot;

	/*assert */
	if(prole == NULL)
	{
		return GPN_SOCK_ROLE_MAN_ERR;
	}
	
	/*get selfSlot*/
	gpnSockCommGetSelfSlot(&selfSlot);
	reVal = GPN_SOCK_ROLE_MAN_OK;

	/*get runPlace */
	if(selfSlot == 0)
	{
		runPlace = GPN_SOCK_RUN_PLACE_IND;
	}
	else
	{
		NMaSlot = 0;
		NMbSlot = 0;
		gpnSockCommGetNMSlot(&NMaSlot, &NMbSlot);
		if(selfSlot == NMaSlot)
		{
			runPlace = GPN_SOCK_RUN_PLACE_NMA;
		}
		else if(selfSlot == NMbSlot)
		{
			runPlace = GPN_SOCK_RUN_PLACE_NMB;
		}
		else
		{
			runPlace = GPN_SOCK_RUN_PLACE_SUB;
		}
	}

	if(reVal == GPN_SOCK_ROLE_MAN_OK)
	{
		*prole = ((funRole & GPN_COMM_ROLE_MODU_MASK) << GPN_COMM_ROLE_MODU_SHIFT) |\
					(runPlace << GPN_COMM_ROLE_RUN_SHIFT);
		
		GPN_SOCK_ROLE_PRINT(GPN_SOCK_ROLE_AGP, "%s : gpn_socket will creat role(0x%08x) \n\r",
			__FUNCTION__, *prole);
	}
	
	return reVal;
}

