UINT32 gpnSockRoleProcNameGet(UINT32 sockRole, char **pprPName, UINT32 *len)
{
	/* role valid check */
	if(sockRole > GPN_SOCK_ALL_ROLE_ID_MAX)
	{
		return GPN_SOCK_ROLE_MAN_ERR;
	}

	if(pprPName != NULL)
	{
		*pprPName = (char *)ggpnSockRoleName[sockRole].processName;
	}

	if(len != NULL)
	{
		*len = strlen(ggpnSockRoleName[sockRole].processName);
	}

	return GPN_SOCK_ROLE_MAN_OK;
}

UINT32 gpnSockRoleShortNameGet(UINT32 sockRole, char **pprSName, UINT32 *len)
{
	/* role valid check */
	if(sockRole > GPN_SOCK_ALL_ROLE_ID_MAX)
	{
		return GPN_SOCK_ROLE_MAN_ERR;
	}

	if(pprSName != NULL)
	{
		*pprSName = (char *)ggpnSockRoleName[sockRole].shortName;
	}

	if(len != NULL)
	{
		*len = strlen(ggpnSockRoleName[sockRole].shortName);
	}

	return GPN_SOCK_ROLE_MAN_OK;
}

UINT32 gpnSockRoleIdGet(char *psockRoleName, UINT32 *psockRole)
{
	UINT32 i;
	
	/* assert */
	if( (psockRoleName == NULL) ||\
		(psockRole == NULL) )
	{
		return GPN_SOCK_ROLE_MAN_ERR;
	}

	for(i=0;i<(GPN_SOCK_ALL_ROLE_ID_MAX+1);i++)
	{
		if(strncmp(psockRoleName,\
				ggpnSockRoleName[i].processName, strlen(psockRoleName)) == 0)
		{
			*psockRole = ggpnSockRoleName[i].sockRoleId;
			return GPN_SOCK_ROLE_MAN_OK;
		}
	}

	return GPN_SOCK_ROLE_MAN_ERR;
}
