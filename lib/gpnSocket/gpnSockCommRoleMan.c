UINT32 gpnSockInstanceInit(gpnRoleDate *pRoleInfo, stSockFdSet *pstFdSet, UINT32 slot)
{
	/*assert*/
	if( (pRoleInfo == NULL) ||\
		(pstFdSet == NULL) )
	{
		return GPN_SOCK_ROLE_MAN_ERR;
	}

	/*step 1: init fdset*/
	gpnSockSelectOptGlobInit(pstFdSet);
	/*step 2: pass moduId to fdset*/
	pstFdSet->moduId = pRoleInfo->moduId;
	pstFdSet->CPUId = pRoleInfo->CPUId;
	pstFdSet->localSrc = pRoleInfo->localSrc;
	pstFdSet->NMaCPUId = pRoleInfo->NMaCPUId;
	pstFdSet->NMbCPUId = pRoleInfo->NMbCPUId;
	
	/*step 3: creat UNIX fd*/
	gpnSockCommCreatUNIXCommRoleFd(pRoleInfo, pstFdSet);
	/*step 4: creat timer-server fd*/
	gpnSockCommCreatUNIXTimerFd(pRoleInfo, pstFdSet);
	/*step 5: creat Transfer-INETfd*/
	gpnSockCommCreatTransferINETFd(pRoleInfo, pstFdSet);
	/*step 6: creat FT-INETfd*/
	gpnSockCommCreatFTINETFd(pRoleInfo, pstFdSet);
	/*step 7: creat NM-SYN-INETfd*/
	gpnSockCommCreatNMSYNINETFd(pRoleInfo, pstFdSet);

	/*step 8: init roler's pig */
	pstFdSet->pid = getpid();
	
	return GPN_SOCK_ROLE_MAN_OK;
}

UINT32 gpnSockCommInit(gpnRoleDate *pRoleInfo, stSockFdSet *pstFdSet, UINT32 funRole)
{
	UINT32 role;
	UINT32 selfSlot;
	INETSockInfo subCliAddr[GPN_COMM_ROLE_MAX_SLOT_NUM];
	
	/*assert*/
	if( (pRoleInfo == NULL) || (pstFdSet == NULL) )
	{
		GPN_SOCK_ROLE_PRINT(GPN_SOCK_ROLE_AGP, "%s : funRole(%08x) para NULL, fatal err!\n\r",\
			__FUNCTION__, funRole);
		return GPN_SOCK_ROLE_MAN_ERR;
	}

	/*role init setp 0: creat role */
	if(gpnSockRoleCreat(funRole, &role) == GPN_SOCK_ROLE_MAN_ERR)
	{
		GPN_SOCK_ROLE_PRINT(GPN_SOCK_ROLE_AGP, "%s : funRole(%08x) gpn role creat err, fatal err!\n\r",\
			__FUNCTION__, funRole);
		return GPN_SOCK_ROLE_MAN_ERR;
	}
	
	/*role init setp 1: init socket communit direction*/
	if(gpnSockRoleInfoCheck(role) == GPN_SOCK_ROLE_MAN_ERR)
	{
		GPN_SOCK_ROLE_PRINT(GPN_SOCK_ROLE_AGP, "%s : gpn role(%08x) invalid, fatal err!\n\r",\
			__FUNCTION__, role);
		return GPN_SOCK_ROLE_MAN_ERR;
	}
	/*role init setp 2: init socket communit direction*/
	gpnSockCommUPPathInit();
	/*role init setp 3: init role  transfer inet socket ip & port */
	gpnCommSubDevCommCfg(subCliAddr);
	/*role init setp 4: get self slot, if dev is dependent device, self slot is 0 */
	gpnSockCommGetSelfSlot(&selfSlot);
	/*role init setp 5: init role  UNIX and INET's socket ip & port to GPN_SOCKET global data struct */
	gpnCommRoleInit(role, selfSlot, pRoleInfo, subCliAddr);
	/*role init setp 6: creat socket fd*/
	gpnSockInstanceInit(pRoleInfo, pstFdSet, selfSlot);

	/*role init setp 7: process signal interrupt timer service:cyc 1 second*/
	gpnSockCommDifTimerInit(role);

	/*role init setp 8:  for socket status be good*/
	sleep(1);
	
	/*printf("pid(%d), gpn_socket init ..................ok!\n\r", getpid());*/
	
	return GPN_SOCK_ROLE_MAN_OK;
}

