UINT32 gpnSockCommMsgDst2RoleSeat(stSockFdSet *pstFdSet, UINT32 dricId, UINT32 *pfdSeat)
{
	UINT32 dstCPU;
	UINT32 dstModu;

	/*assert*/
	if( (pstFdSet == NULL) ||\
		(pfdSeat == NULL) )
	{
		return GPN_SELECT_GEN_ERR;
	}
	
	dstCPU = GPN_SOCK_DECOM_CPUID(dricId);
	dstModu = GPN_SOCK_DECOM_MODUID(dricId);
	if( (dstCPU > GPN_COMM_CPUID_SLOT_20) ||\
		/*(dstCPU < GPN_COMM_CPUID_SLOT_00) ||\*/
		(dstModu > GPN_SOCK_INS_ROLE_ID_MAX) )
	{
		return GPN_SELECT_GEN_ERR;
	}
	
	if(pstFdSet->CPUId == dstCPU)
	{
		/* msg transfer in one cpu */
		*pfdSeat = dstModu;
	}
	else
	{
		/* msg transfer between cpus */
	    if((pstFdSet->moduId == GPN_SOCK_ROLE_TRANS))
	    {
			/*gpn_transfer_role switch MSGs between CPUs */
			*pfdSeat = (GPN_SOCK_ROLE_NM_TO_SUB1T - 1) + dstCPU;
	    }
		else if((pstFdSet->moduId == GPN_SOCK_ROLE_FT) &&\
			 	(dstModu == GPN_SOCK_ROLE_FT))
		{
			/*gpn_ft_role switch MSGs between FT for file transfer */
			*pfdSeat = (GPN_SOCK_ROLE_NM_TO_SUB1F - 1) + dstCPU;
		}
		else if((pstFdSet->moduId == GPN_SOCK_ROLE_NM_SYN) &&\
				(dstModu == GPN_SOCK_ROLE_NM_SYN))
		{
			/*gpn_nmsyn_role switch MSGs between SYN-process*/
			if(dstCPU == pstFdSet->NMaCPUId)
			{
				/* if dst is NMa, then syn fd creat by NMb, so use NMb syn fdSeat */
				*pfdSeat = GPN_SOCK_ROLE_SYN_TO_NMB;
			}
			else if(dstCPU == pstFdSet->NMbCPUId)
			{
				/* if dst is NMb, then syn fd creat by NMa, so use NMa syn fdSeat */
				*pfdSeat = GPN_SOCK_ROLE_SYN_TO_NMA;
			}
			else
			{
				/* error dstCPU */
				return GPN_SELECT_GEN_ERR;
			}
		}
		else
		{
			/*other role swicth MSGs to gpn_transfer_role wish reach to other CPU */
			*pfdSeat = GPN_SOCK_ROLE_TRANS;
		}
	}
	
	GPN_SOCK_ROLE_PRINT(GPN_SOCK_ROLE_AGP, "%s : (localCPU %d localModu %d) -> (dstCPU %d dstModu %d), get fdSeat %d\n\r",\
		__FUNCTION__, pstFdSet->CPUId, pstFdSet->moduId, dstCPU, dstModu, *pfdSeat);

	return GPN_SELECT_GEN_OK;
}

