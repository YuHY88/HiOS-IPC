UINT32 gpnSockCommCreatUNIXCommRoleFd(gpnRoleDate *pRoleInfo, stSockFdSet *pstFdSet)
{
	INT32 i;
	INT32 socketFd;
	UINT32 fdSeat;
	stGpnFdAddr fdAddr;

	fdSeat = GPN_COMMM_NULL;
	for(i=0;i<(GPN_SOCK_INS_ROLE_ID_MAX+1);i++)
	{
		memset(&fdAddr, 0, sizeof(stGpnFdAddr));
		if(pRoleInfo->moduUNIXSockInfo[i].sockSta == GPN_SOCK_FD_ROLE_SYN_SEV)
		{
			socketFd = gpnUnixUdpCommServer(pRoleInfo->moduUNIXSockInfo[i].addrSev.sun_path);
			if(socketFd == -1)
			{
				GPN_SOCK_ROLE_PRINT(GPN_SOCK_ROLE_CUP, "%s : slot(%d) creat INET fd err!\n\r",\
					__FUNCTION__, i+1);
				return GPN_SOCK_ROLE_MAN_ERR;
			}

			fdAddr.fdRole = GPN_SOCKET_OLD_UDP_UNIX_SER;
			memcpy(	(void *)&(fdAddr.selfAddr),\
					(void *)&(pRoleInfo->moduUNIXSockInfo[i].addrSev),\
					sizeof(pRoleInfo->moduUNIXSockInfo[i].addrSev));
			memcpy(	(void *)&(fdAddr.defConnAddr),\
					(void *)&(pRoleInfo->moduUNIXSockInfo[i].addrSev),\
					sizeof(pRoleInfo->moduUNIXSockInfo[i].addrSev));
			if(gpnSockCommXID2UnixCommRoleSeat(i, &fdSeat) == GPN_SOCK_ROLE_MAN_ERR)
			{
				return GPN_SOCK_ROLE_MAN_ERR;
			}
			if(gpnSockSelectFdtoFdSet(pstFdSet, socketFd, fdSeat, &fdAddr) == GPN_SOCK_ROLE_MAN_ERR)
			{
				return GPN_SOCK_ROLE_MAN_ERR;
			}
		}
		else if(pRoleInfo->moduUNIXSockInfo[i].sockSta == GPN_SOCK_FD_ROLE_CLI)
		{
			socketFd = gpnUnixUdpCommClient(pRoleInfo->moduUNIXSockInfo[i].addrCli.sun_path);
			if(socketFd == -1)
			{
				return GPN_SOCK_ROLE_MAN_ERR;
			}

			fdAddr.fdRole = GPN_SOCKET_OLD_UDP_UNIX_CLI;
			memcpy(	(void *)&(fdAddr.selfAddr),\
					(void *)&(pRoleInfo->moduUNIXSockInfo[i].addrCli),\
					sizeof(pRoleInfo->moduUNIXSockInfo[i].addrCli));
			memcpy(	(void *)&(fdAddr.defConnAddr),\
					(void *)&(pRoleInfo->moduUNIXSockInfo[i].addrSev),\
					sizeof(pRoleInfo->moduUNIXSockInfo[i].addrSev));
			if(gpnSockCommXID2UnixCommRoleSeat(i, &fdSeat) == GPN_SOCK_ROLE_MAN_ERR)
			{
				return GPN_SOCK_ROLE_MAN_ERR;
			}
			if(gpnSockSelectFdtoFdSet(pstFdSet, socketFd, fdSeat, &fdAddr) == GPN_SOCK_ROLE_MAN_ERR)
			{
				return GPN_SOCK_ROLE_MAN_ERR;
			}
		}
		else
		{
			/*do nothing*/
			continue;
		}

		GPN_SOCK_ROLE_PRINT(GPN_SOCK_ROLE_CUP, "role(0x%08x), modu(%d)\n\r", pRoleInfo->role, i);
		GPN_SOCK_ROLE_PRINT(GPN_SOCK_ROLE_CUP, "fd(%08x),selfAddr \"%s\"\n\r", socketFd, fdAddr.selfAddr.sun_path);
		GPN_SOCK_ROLE_PRINT(GPN_SOCK_ROLE_CUP, "fd(%08x),defConnAddr \"%s\"\n\r", socketFd, fdAddr.defConnAddr.sun_path);
		GPN_SOCK_ROLE_PRINT(GPN_SOCK_ROLE_CUP, "fd(%08x),newConnAddr \"%s\"\n\r", socketFd, fdAddr.newConnAddr.sun_path);
	}
	return GPN_SOCK_ROLE_MAN_OK;
}
